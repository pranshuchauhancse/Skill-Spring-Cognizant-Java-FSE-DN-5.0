package Upskilling.Module3Java;

public class DataTypeDemonstration {
    
    public static void main(String[] args) {

        // Primitive data types
        int number = 100;
        float decimal1 = 12.5f;
        double decimal2 = 99.99;
        char grade = 'A';
        boolean isPassed = true;

        // Display values
        System.out.println("Integer Value : " + number);
        System.out.println("Float Value   : " + decimal1);
        System.out.println("Double Value  : " + decimal2);
        System.out.println("Character     : " + grade);
        System.out.println("Boolean Value : " + isPassed);

    }

}